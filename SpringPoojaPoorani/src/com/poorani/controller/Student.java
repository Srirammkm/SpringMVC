package com.poorani.controller;

public class Student {
   private Integer pass;
   private String name;

   public void setPass(Integer pass) {
      this.pass = pass;
   }
   public Integer getPass() {
      return pass;
   }
   public void setName(String name) {
      this.name = name;
   }
   public String getName() {
      return name;
   }
}